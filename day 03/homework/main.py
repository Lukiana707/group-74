# Step 1: Create a variable x
x = 5

# Step 2: Create another variable y
y = 2.5

# Step 3: Print x + y
print(x + y)  # Output: 7.5

# Step 4: Comment about the types
# x is an integer (int), y is a floating-point number (float)

# Step 5: Print their types
print(type(x))  # <class 'int'>
print(type(y))  # <class 'float'>