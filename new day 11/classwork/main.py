# 1) FLOWCHART, ALGORITHM, NATURAL LANGUAGE, PSEUDOCODE

# A flowchart is a visual diagram that represents the steps of a process or algorithm.
# It uses symbols like rectangles (for processes), diamonds (for decisions), and arrows (for flow direction).
# Flowcharts help people understand logic without needing to read code.

# An algorithm is a step-by-step set of instructions to solve a problem or perform a task.
# Algorithms can be written in many forms: code, pseudocode, or even natural language.

# Natural language means describing the steps in everyday human language.
# Example: "First, boil water. Then, add pasta. Cook for 10 minutes."

# Pseudocode is a simplified, informal way to describe an algorithm using structured logic.


# A sequence is a instructions that are executed one after another in order like step by step.
# Example:
print("Step 1: open your computer or tablet ")
print("Step 2: Open the browser/ google")
print("Step 3: Visit your favorite website")


# Iteration means repeating a set of instructions multiple times, usually using loops like it can print your name or the thing you have wrote as many times as you want.
# like this example
for i in range(1000):
    print("Hello")


# Selection means making decisions in code using conditions (like if/else).
# Example: Check if a number is positive or negative
number = -3
if number >= 0:
    print("Positive number")
else:
    print("Negative number")

